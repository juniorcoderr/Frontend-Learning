// The DOM represents HTML as a tree of nodes
/*
<!DOCTYPE html>
<html>
 <head>
 <title>Page Title</title>
 </head>
 <body>
 <h1 id="main-title">Welcome</h1>
 <p class="description">This is a paragraph</p>
 <ul>
 <li>Item 1</li>
 <li>Item 2</li>
 </ul>
 </body>
</html>
*/

// DOM Tree Structure:
// Document
// └── html (Element Node)
// ├── head (Element Node)
// │ └── title (Element Node)
// │ └── "Page Title" (Text Node)
// └── body (Element Node)
// ├── h1 (Element Node)
// │ └── "Welcome" (Text Node)
// ├── p (Element Node)
// │ └── "This is a paragraph" (Text Node)
// └── ul (Element Node)
// ├── li (Element Node)
// │ └── "Item 1" (Text Node)
// └── li (Element Node)
// └── "Item 2" (Text Node)
